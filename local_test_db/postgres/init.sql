-- PostgreSQL 16 initialization: schema and deterministic seed data.
CREATE TABLE plugin_test_users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR(100) NOT NULL UNIQUE,
    email VARCHAR(255) NULL,
    department VARCHAR(100) NULL,
    salary NUMERIC(12, 2) NULL,
    created_at TIMESTAMP NOT NULL
);
CREATE INDEX idx_plugin_test_users_department ON plugin_test_users (department);

CREATE TABLE plugin_test_orders (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES plugin_test_users (id),
    product_name VARCHAR(200) NOT NULL,
    amount NUMERIC(12, 2) NOT NULL,
    status VARCHAR(20) NOT NULL CHECK (status IN ('completed', 'pending', 'cancelled')),
    created_at TIMESTAMP NOT NULL
);
CREATE INDEX idx_plugin_test_orders_status ON plugin_test_orders (status);
CREATE INDEX idx_plugin_test_orders_user_id ON plugin_test_orders (user_id);

CREATE TABLE plugin_test_logs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    event_type VARCHAR(100) NOT NULL,
    event_message TEXT NOT NULL,
    created_at TIMESTAMP NOT NULL
);
CREATE INDEX idx_plugin_test_logs_created_at ON plugin_test_logs (created_at);
CREATE INDEX idx_plugin_test_logs_event_type ON plugin_test_logs (event_type);

